import java.util.Scanner;

public class Condition {
  public static void main(String[] args) {
    int a = 10;
    if (a == 10){
      System.out.println("Hello World");
    }

    // While Loop
    int count = 1;
    while (count != 6){
      System.out.println(count);
      count++;
    }

    // For Loop
    for(int counts = 1; counts != 5; counts++){
      System.out.println(count);
    }
  }
}
